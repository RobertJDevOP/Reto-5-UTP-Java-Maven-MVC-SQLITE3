    private void rSButtonMaterialGradientOne3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonMaterialGradientOne3ActionPerformed
        //Btn 1
        LblSubTitulo.setText("Informaci√≥n lideres del proyecto");
        String[] Headers = {"Nombre", "Apellido", "Nro", "Salario"};
        ControladorRequerimientosReto4 controller = new ControladorRequerimientosReto4();

        ArrayList<Requerimiento1> lista;

        try {
            lista = controller.consultarLiderPorSalarios();

            Object[][] data = new Object[lista.size()][4];
            int index = 0;

            for (Requerimiento1 r : lista) {
                data[index][2] = r.getIdLider();
                data[index][0] = r.getNombre();
                data[index][1] = r.getPrimerApellido();
                data[index][3] = r.getSalario();
                index++;
            }

            DefaultTableModel model = new DefaultTableModel(data, Headers);
            JTableExample.setModel(model);
        } catch (SQLException e) {
            System.err.println(e);
        }

    }//GEN-LAST:event_rSButtonMaterialGradientOne3ActionPerformed

